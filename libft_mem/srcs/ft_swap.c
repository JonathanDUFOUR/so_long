#include <stdint.h>

void	ft_swap(void *a, void *b)
{
	*(uint64_t *)a ^= *(uint64_t *)b;
	*(uint64_t *)b ^= *(uint64_t *)a;
	*(uint64_t *)a ^= *(uint64_t *)b;
}
