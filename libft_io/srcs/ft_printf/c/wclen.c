#include <wchar.h>
#include "typedefs/t_int.h"

t_uint	wclen(wchar_t c)
{
	t_uint	len;

	len = 1;
	if (c >= 0x80 && ++len && c >= 0x800 && ++len && c >= 0x10000)
		++len;
	return (len);
}
